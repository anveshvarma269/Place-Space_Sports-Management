    private void btnAccountActivateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccountActivateActionPerformed
        // TODO add your handling code here:
        String username = txtUname.getText();
        String password = txtPwd.getText();
        String cnfrmPwd = txtCnfrmPwd.getText();
        String passcode = txtasscode.getText();

        UserAccount ua = ud.getUserToGrantAccess(username);
        if(ua == null)
        {
            JOptionPane.showMessageDialog(this, "Username invalid");
        }
        else
        {
            if(ua.getPasscode().equals(passcode))
            {
                if(password.equals(cnfrmPwd))
                {
                    ua.setPassword(password);
                    JOptionPane.showMessageDialog(this, "Account activated! Proceed to login");
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Passwords dont match");
                }
            }
            else
            JOptionPane.showMessageDialog(this, "Invalid passcode");
        }
        db.storeSystem(ecosystem);
        txtUname.setText("");
        txtCnfrmPwd.setText("");
        txtPwd.setText("");
        txtasscode.setText("");
    }//GEN-LAST:event_btnAccountActivateActionPerformed


