    private void populateTable() {
        DefaultTableModel df = (DefaultTableModel) tblEquipment.getModel();
        df.setRowCount(0);
        
        HashMap<String,Integer> equipment = supplier.getEquipment();
        
        for(String s: equipment.keySet())
        {
            String[] row = {s,String.valueOf(equipment.get(s))};
            df.addRow(row);
        }
        tblEquipment.setModel(df);
    }
