    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        HashMap<String,Integer> equipment = supplier.getEquipment();
        
        if(equipment.containsKey(txtEqType.getText()))
        {
            equipment.put(txtEqType.getText(), equipment.get(txtEqType.getText()) + Integer.parseInt(txtAvilbility.getText()));
        }
        else
        equipment.put(txtEqType.getText(), Integer.parseInt(txtAvilbility.getText()));
        populateTable();
        txtAvilbility.setText("");
        txtEqType.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed


