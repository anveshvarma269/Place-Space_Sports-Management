    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel df = (DefaultTableModel) tblRequestTravel.getModel();
        if(txtDate.getDate() == null) return;
        int selectedRow = tblRequestTravel.getSelectedRow();
        if(selectedRow == -1) {JOptionPane.showMessageDialog(this, "Select traveller");return;}
        
        String trName = df.getValueAt(selectedRow,0).toString();
        TravelTransport tt = suppDir.getTravelTransport(trName);
        Request request = new Request(team.getTeamName(), trName);
        request.setLocation(txtDest.getText());
        request.setDate(txtDate.getDate());
        request.setTravelAmount(Integer.parseInt(txtAmount.getText()));
        suppDir.addTravelRequest(request);
        JOptionPane.showMessageDialog(this, "Travel requested");
        txtAmount.setText("");
        txtDest.setText("");
        txtDate.setDate(null);
    }//GEN-LAST:event_jButton1ActionPerformed

