    public  void populateTable() {
        //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
         DefaultTableModel dx = (DefaultTableModel) tblRequestTravel.getModel();
        dx.setRowCount(0);
        ArrayList<TravelTransport> travels = suppDir.getTravelTransport();
        HashMap<String,Integer> transpot;
        for(TravelTransport t : travels)
        {
            
            if(t.getTransportTypes() == null) t.setTransportTypes(new HashMap<String,Integer>());
             transpot = t.getTransportTypes();
            for(String s : transpot.keySet())
            {
                String[] row = {t.getTransportName(),s,String.valueOf(transpot.get(s))};
                dx.addRow(row);
            }
                
        }
        tblRequestTravel.setModel(dx);
        
        
    }
